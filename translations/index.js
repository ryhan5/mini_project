const defaultTranslations = translations.en;

/**
 * Get translation for a given key and language
 * Supports nested keys using dot notation (e.g., 'nav.home')
 */
const t = (key, lang = 'en') => {
  // If no key is provided, return empty string
  if (!key) return '';
  
  // Get the translation object for the specified language, fallback to English
  const langTranslations = translations[lang] || translations.en;
  
  // Handle nested keys (e.g., 'nav.home')
  const keys = key.split('.');
  let result = langTranslations;
  
  try {
    // Traverse the nested object structure
    for (const k of keys) {
      if (result === undefined || result === null) break;
      result = result[k];
    }
    
    // If we found a result, return it
    if (result !== undefined && result !== null) {
      return result;
    }
    
    // Fallback to English if the key is not found in the specified language
    if (lang !== 'en') {
      result = translations.en;
      for (const k of keys) {
        if (result === undefined || result === null) break;
        result = result[k];
      }
      if (result !== undefined && result !== null) {
        return result;
      }
    }
    
    // If still not found, return the last part of the key as a fallback
    return keys[keys.length - 1];
  } catch (error) {
    console.error(`Translation error for key "${key}":`, error);
    return key;
  }
};

// Export the translations object as default
export default translations;

// Named export for the t function
export { t };
